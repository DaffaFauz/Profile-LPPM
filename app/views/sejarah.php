            Jurnal MAPS (Manajemen Perbankan Syariah); untuk Prodi Perbankan
            Syariah;
        </li>
        <li>
            Jurnal AIMS (Accounting Information System); untuk Prodi Komputerisasi
            Akuntansi;
        </li>
        <li>
            Jurnal INTERNAL (Information System Journal); untuk Prodi Manajemen
            Informatika.
        </li>
    </ol>
    <p style="text-align: justify; text-indent: 45px">
        Selain mengelola jurnal, UPPM juga mengelola KKN (Kuliah Kerja Nyata)
