        STKOM Al Ma’soem, dimana kegiatan Penelitian dan Pengabdian kepada
        Masyarakat (PPkM) dilaksanakan oleh UPPM (Unit Penelitian dan Pengabdian
        kepada Masyarakat) yang dirintis sejak April 2017. Saat itu, UPPM
        membentuk sebuah rumah jurnal dengan laman
        <a href="https://ojs.journalstkomalmasoem.ac.id" target="_blank"
            rel="noopener noreferrer">https://ojs.journalstkomalmasoem.ac.id</a>, didalamnya terdapat tiga jurnal yang
        bernama :
    </p>
    <ol type="1">
