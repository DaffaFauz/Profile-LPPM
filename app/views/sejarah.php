<section class="body container mb-5">
    <p style="text-align: justify; text-indent: 45px">
        Tri Dharma Perguruan Tinggi adalah tiga pilar utama yang menjadi
        landasan dalam penyelenggaraan pendidikan tinggi di Indonesia, yaitu
        Pendidikan, Penelitian dan Pengabdian kepada Masyarakat. Konsep ini
        tidak hanya berlaku bagi dosen, tetapi juga menjadi tanggung jawab
        seluruh sivitas akademika dalam menciptakan pendidikan tinggi yang
        bermutu dan berkontribusi nyata bagi masyarakat dan negara. Pengelolaan
        bidang Penelitian dan Pengabdian kepada Masyarakat di Universitas
        Ma’soem berada dibawah LPPM Universitas Ma’soem.
    </p>
    <br />
    <p style="text-align: justify; text-indent: 45px">
