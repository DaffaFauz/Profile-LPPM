        Mahasiswa disebar ke Desa-desa yang ada di Kabupaten Bandung. Sejalan
        dengan perkembangan IPTEKS dan perubahan bentuk dari STKOM dan STIBANKS
        AL Ma’soem menjadi Universitas Ma’soem, akhirnya pada tahun 2019 UPPM
